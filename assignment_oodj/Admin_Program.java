    private void updateTable(){
        if(SelectUserType.getSelectedItem().equals("All")){
            currentAdmin.updateUserInfoTable(userInfo, SelectUserID);
        }else{
            currentAdmin.updateUserInfoTable(userInfo,SelectUserType.getSelectedItem().toString(), SelectUserID);
        }
    }
