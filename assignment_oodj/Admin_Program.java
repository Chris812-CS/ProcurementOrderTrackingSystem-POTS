    private void ErrorLabels(){
        errorLabels.clear();
        errorLabels.add(FNError);
        errorLabels.add(LNError);
        errorLabels.add(EmailError);
        errorLabels.add(GenderError);
        errorLabels.add(ContactError);
        errorLabels.add(RegisterError);
    }
    
    private void ClearLabels(){
        RegisterUID.setText("");
        RegisterFN.setText("");
        RegisterLN.setText("");
        RegisterEmail.setText("");
        RegisterContact.setText("");
        RegisterGender.clearSelection();
    }
    
