    private void infoEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoEditActionPerformed
        JTextField allFields[] = {infoFN,infoLN,infoGender,infoEmail,infoContact,infoPassword};
        if(infoEdit.isSelected()){
            infoSave.setEnabled(true);
            infoDelete.setEnabled(true);
            for(JTextField field: allFields){
                field.setEditable(true);
                field.setBackground(new Color(214,192,179));
                field.setFocusable(true);
            }
            SelectUserID.setEnabled(false);
            SelectUserType.setEnabled(false);
            jTable1.setEnabled(false);
        }else{
            infoSave.setEnabled(false);
            infoDelete.setEnabled(false);
               for(JTextField field: allFields){
                   field.setEditable(false);
                   field.setBackground(new Color(179,164,155));
                   field.setFocusable(false);
                }
            SelectUserID.setEnabled(true);
            SelectUserType.setEnabled(true);
            jTable1.setEnabled(true);
        }
    }//GEN-LAST:event_infoEditActionPerformed

    private void infoSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoSaveActionPerformed
        JTextField allFields[] = {infoFN,infoLN,infoGender,infoEmail,infoContact,infoPassword};
        if(SelectUserID.getSelectedItem() == null){
             javax.swing.JOptionPane.showMessageDialog(null, "No user ID selected !", "Select User ID", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String ID = SelectUserID.getSelectedItem().toString();
        currentAdmin.updateProfile(infoFN.getText(), infoLN.getText(), infoEmail.getText(), infoContact.getText(), infoGender.getText().toUpperCase(), SelectUserID.getSelectedItem().toString(), infoPassword.getText());        
        updateTable();
        
        SelectUserID.setSelectedItem(ID);
        currentAdmin.DisplayUserInfo(ID,allFields);
        
    }//GEN-LAST:event_infoSaveActionPerformed

    private void infoDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoDeleteActionPerformed
        if(SelectUserID.getSelectedItem() == null){
             javax.swing.JOptionPane.showMessageDialog(null, "No user ID selected !", "Select User ID", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        Object options[] = {"Yes", "No"};
        int choice = javax.swing.JOptionPane.showOptionDialog(null, "This user will be deleted. Confirm?", "Are you sure?" ,javax.swing.JOptionPane.YES_NO_OPTION,javax.swing.JOptionPane.QUESTION_MESSAGE,null,options, "No");
        if(choice == javax.swing.JOptionPane.YES_OPTION){
            currentAdmin.deleteUser(SelectUserID.getSelectedItem().toString());
            updateTable();
            
        }else{
            return;
        }
    }//GEN-LAST:event_infoDeleteActionPerformed

